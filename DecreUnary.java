class DecreUnary {
    public static void main(String args[]){
        int i = 100;
        --i;       //99
        i--;       //98
        --i;       //97
        --i;       //96
        i--;       //95
        i--;       //94
        --i;       //93
        i--;       //92
        System.out.println(i);
    }
}
