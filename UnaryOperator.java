public class UnaryOperator {
public static void main(String args[])  {
    int i = 2;
    i++;        //3
    ++i;        //4
    ++i;       //5
    ++i;       //6
    i++;       //7
    i++;        //8
    ++i;     //9
    ++i;         //10
    i++;     //11
    System.out.println(i);
}  
}


